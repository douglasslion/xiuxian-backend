/**
 * 排行榜模型
 */

const mongoose = require('mongoose');

const rankingSchema = new mongoose.Schema({
  userId: {
    type: String,
    required: true,
    unique: true
  },
  username: {
    type: String,
    required: true
  },
  score: {
    type: Number,
    required: true,
    default: 0
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
});

const Ranking = mongoose.model('Ranking', rankingSchema);

module.exports = Ranking;
